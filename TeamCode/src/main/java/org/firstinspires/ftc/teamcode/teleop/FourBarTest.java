@TeleOp(name="Four Bar Test", group="Test")

public class FourBarTest extends LinearOpMode {

    // Declare OpMode members.
    private Servo fourBarLeft = null;
    private Servo fourBarRight = null;

    @Override
    public void runOpMode() {
        telemetry.addData("Status", "Initialized");
        telemetry.update();

        // Initialize the hardware variables. Note that the strings used here as parameters
        // to 'get' must correspond to the names assigned during the robot configuration
        // step (using the FTC Robot Controller app on the phone).
        fourBarLeft = hardwareMap.get(Servo.class, "fourBarLeft");
        fourBarRight = hardwareMap.get(Servo.class, "fourBarRight");


        // Most robots need the motor on one side to be reversed to drive forward
        // Reverse the motor that runs backwards when connected directly to the battery

        fourBarLeft.setDirection(Servo.Direction.REVERSE);
        fourBarRight.setDirection(Servo.Direction.FORWARD);

        // Wait for the game to start (driver presses PLAY)
        waitForStart();

        // run until the end of the match (driver presses STOP)
        while (opModeIsActive()) {

//            if (gamepad1.cross) {
//                fourBarLeft.setPosition(0);
//                fourBarRight.setPosition(0);
//            }
//
//            if (gamepad1.square) {
//                fourBarLeft.setPosition(1);
//                fourBarRight.setPosition(1);
//            }

            fourBarLeft.setPosition(-gamepad1.left_stick_y);
            fourBarRight.setPosition(-gamepad1.left_stick_y);

            telemetry.addData("Servo Pos:", -gamepad1.left_stick_y);
            telemetry.update();
        }
    }
}
